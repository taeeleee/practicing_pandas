data_sales = {
    '직원': ['김대리', '이과장', '박사원', '최부장', '정인턴'],
    '부서': ['영업1팀', '영업2팀', '영업1팀', '영업2팀', '영업1팀'],
    '판매액':[ 500, 1200, 300, 2000, 100],
    '근무년수': [3, 7, 1, 15, 0]
}

df_sales = pd.DataFrame(data_sales)

print("영업팀 실적 데이터 준비 完")
print(df_sales)

"""### **1교시. "조건"에 맞는 데이터만 남기기 (Filtering)**
엑셀의 '필터' 기능과 똑같습니다. "판매액이 1000만 원 이상인 사람만 보여줘!"를 코드로 짜는 겁니다.


**`df[ 조건식 ]`**

→ 데이터프레임(df)에게 대괄호 안에 [ 조건식 ] 이 **참(True)** 인 녀석들만 남기라고 명령하는 겁니다.

"""

#[👨‍🏫 연습문제] 근무년수가 5년 이상인 베테랑만 뽑아볼까요?
print(df_sales[df_sales['근무년수']>=5])

#[👨‍🏫 연습문제] 판매액이 1000 이상인 행만 필터링하기**
#'판매액'이 1000만 원 이상(>=)** 인 고성과자 직원들만 뽑아서 확인해 보세요.

high_performer = df_sales[df_sales['판매액']>= 1000]
print(high_performer)

"""### **2교시. "순서"대로 줄 세우기 (Sorting)**
데이터를 크기순으로 나열하는 겁니다.


작은 것 → 큰 것 (오름차순): 기본값


큰 것 → 작은 것 (내림차순): `ascending=False` (이게 중요!)


```
df.sort_values(by='기준컬럼', ascending=False)
```
"""

#[👨‍🏫 연습문제] 근무년수가 긴 순서대로(내림차순) 정렬해볼까요?
df_sales.sort_values(by='근무년수', ascending=False)

#[👨‍🏫 연습문제] 이번 달 '판매액'이 가장 높은 순서대로(1등이 맨 위에 오게) 직원들을 줄 세워 보세요.
df_sales.sort_values(by='판매액', ascending=False)

#[👨‍🏫 연습문제] 이번 달 '판매액'이 가장 높은 순서대로(1등이 맨 위에 오게) 직원들을 줄 세우고, 변수를 지정해주세요.
top_sales = df_sales.sort_values(by='판매액', ascending=False)
print(top_sales)

"""###**3교시. 까다로운 조건 (AND 연산자)**
사장님이 더 까다로운 주문을 합니다. "영업1팀 중에서 실적이 400만 원 넘는 사람만 데려와."

조건이 두 개죠?

이때는 & (그리고) 기호를 씁니다.

---

**[주의할 점]**

조건들을 반드시 **소괄호 ( )** 로 감싸줘야 합니다. 안 그러면 에러 납니다!

**`df[ (조건1) & (조건2) ]`**
"""

#[👨‍🏫 연습문제] '영업2팀'이면서 '근무년수가 10년 이상'인 사람을 찾아봅시다.
df_sales[(df_sales['부서']== '영업2팀') & (df_sales['근무년수']>=10)]

#[👨‍🏫 연습문제] 이번엔 우리 팀(영업1팀)의 유망주를 찾습니다.
# 조건 1: 부서가 '영업1팀' 이고 (== '영업1팀') 조건 2: 판매액이 400만 원 이상인 (>= 400) 직원을 찾아주세요.
# 두 조건에 부합하는 직원을 'target staff'로 변수 지정해주세요.
target_staff = df_sales[(df_sales['부서']=='영업1팀')&(df_sales['판매액']>= 400)]
print(target_staff)

